static void SystemClock_Config(void);
static void PeriphCommonClock_Config(void);




bool bspInit(void)
{
  bool ret = true;


  HAL_Init();


  SystemClock_Config();
  PeriphCommonClock_Config();

  return ret;
}

bool bspDeInit(void)
{
  // usbDeInit();
  delay(50);

  // Disable Interrupts
  //
  for (int i=0; i<sizeof(NVIC->ICER)/(sizeof(uint32_t)); i++)
  {
    NVIC->ICER[i] = 0xFFFFFFFF;
    __DSB();
    __ISB();
  }
  SysTick->CTRL = 0;
    
  return true;
}

void delay(uint32_t ms)
{
#ifdef _USE_HW_RTOS
  if (xTaskGetSchedulerState() != taskSCHEDULER_NOT_STARTED)
  {
    osDelay(ms);
  }
  else
  {
    HAL_Delay(ms);
  }
#else
  HAL_Delay(ms);
#endif
}

uint32_t millis(void)
{
  return HAL_GetTick();
}



